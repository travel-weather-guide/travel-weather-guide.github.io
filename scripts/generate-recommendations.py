# 3. 점수 산출 함수
# ============================================================
def calc_temp_score(high, low):
    """기온 점수 (0-30). 평균 20-26°C가 최적."""
    avg = (high + low) / 2
    if 20 <= avg <= 26:
        return 30
    elif 17 <= avg < 20 or 26 < avg <= 29:
        return 24
    elif 14 <= avg < 17 or 29 < avg <= 32:
        return 18
    elif 10 <= avg < 14 or 32 < avg <= 35:
        return 12
    elif 5 <= avg < 10 or 35 < avg <= 38:
        return 6
    else:  # <5 or >38
        return 0


def calc_rainfall_score(mm):
    """강수량 점수 (0-25). 낮을수록 좋음."""
    if mm < 30:
        return 25
    elif mm < 60:
        return 20
    elif mm < 100:
        return 15
    elif mm < 150:
        return 10
    elif mm < 250:
        return 5
    else:
        return 0


def calc_rainydays_score(days):
    """강수일수 점수 (0-15)."""
    if days < 5:
        return 15
    elif days < 8:
        return 12
    elif days < 12:
        return 8
    elif days < 16:
        return 4
    else:
        return 0


def calc_humidity_score(pct):
    """습도 점수 (0-15). 낮을수록 쾌적."""
    if pct < 55:
        return 15
    elif pct < 65:
        return 12
    elif pct < 75:
        return 8
    elif pct < 85:
        return 4
    else:
        return 0


def calc_sunshine_score(hours):
    """일조시간 점수 (0-15). 길수록 좋음."""
    if hours >= 10:
        return 15
    elif hours >= 8:
        return 12
    elif hours >= 6:
        return 8
    elif hours >= 4:
        return 4
    else:
        return 0


def calc_score(region_id, month, weather):
    """총 점수 산출 (0-100). 기후 점수 - 자연재해 감점."""
    climate = (
        calc_temp_score(weather["tempHigh"], weather["tempLow"])
        + calc_rainfall_score(weather["rainfall"])
        + calc_rainydays_score(weather["rainyDays"])
        + calc_humidity_score(weather["humidity"])
        + calc_sunshine_score(weather["sunshineHours"])
    )
    penalty = (
        TYPHOON_PENALTY.get((region_id, month), 0)
        + FLOOD_PENALTY.get((region_id, month), 0)
    )
    return max(0, climate - penalty)


def score_to_rating(score):
    """점수 → 1-5 rating."""
    if score >= 80:
        return 5
    elif score >= 65:
        return 4
    elif score >= 50:
        return 3
    elif score >= 35:
        return 2
    else:
        return 1


